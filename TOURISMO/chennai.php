        <p class="card-text">Fort St George (or historically, White Town) is the first English (later British) fortress in India, founded in 1644
		at the coastal city of Madras, the modern city of Chennai. The construction of the fort provided the impetus for further settlements and trading 
		activity, in what was originally an uninhabited land. Thus, it is a feasible contention to say that the city evolved around the fortress.
		The fort currently houses the Tamil Nadu legislative assembly and other official buildings.</p>
        
